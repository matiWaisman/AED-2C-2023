public class tupla {
    private int valor;
    private int apariciones;

    // Constructor
    public tupla(int valor, int apariciones) {
        this.valor = valor;
        this.apariciones = apariciones;
    }

    
    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    
    public int getApariciones() {
        return apariciones;
    }

    public void setApariciones(int apariciones) {
        this.apariciones = apariciones;
    }
}

